package Daily_Practise.Leetcode.Leetcode_Array;

import java.util.Scanner;

public class The_two_sneaky_numbers_of_digitville {
    public static void main(String[] args) {
        Scanner sc = new Scanner(System.in);
        int n = sc.nextInt();
        int[] nums = new int[n];
        for (int i = 0; i < n; i++) {
            nums[i] = sc.nextInt();
        }
    }
}
